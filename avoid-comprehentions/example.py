# Avoid Comprehentions(too much short code) 

# List or dictionary comprehentions are good for large data sets, or the data sets, where performance gets impacted if we do not use some type of comprehentions
# According to a programming fact, about 90% time is reading our own code, and 10% of the time is spent coding our actual code, so it is better to avoid too much comprehentions, as we may get confused after watching the code, after some time duration
